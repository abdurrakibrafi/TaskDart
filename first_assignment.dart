void main() {
    const int a = 7; //declare with constant value
    const int b = 3; //declare with constant value

    // for Addition
    int _addition = a + b;
    //using string interpulation we print the message in console
    print('Addition: $a + $b = $_addition');

    // for Subtraction
    int _subtraction = a - b;
    print('Subtraction: $a - $b = $_subtraction');

    // for Multiplication
    int _multiplication = a * b;
    print('Multiplication: $a * $b = $_multiplication');

    // for division
    double _division = a / b;
    print('Division: $a / $b = $_division');

    // for Modulus
    int _modulus = a % b;
    print('Modulus: $a % $b = $_modulus');
  }
